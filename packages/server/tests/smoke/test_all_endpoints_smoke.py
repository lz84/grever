class TestAllEndpointsNo500:
    """Smoke test: every endpoint must NOT return 500."""

    @pytest.mark.parametrize("method,path,body", ENDPOINTS)
    def test_no_500(self, client: TestClient, method: str, path: str, body):
        """Hit endpoint and verify status code is not 500."""
        if method == "GET":
            resp = client.get(path)
        elif method == "POST":
            resp = client.post(path, json=body if body is not None else {})
        elif method == "PUT":
            resp = client.put(path, json=body if body is not None else {})
        elif method == "PATCH":
            resp = client.patch(path, json=body if body is not None else {})
        elif method == "DELETE":
            resp = client.delete(path)
        elif method == "HEAD":
            resp = client.head(path)
        else:
            pytest.skip(f"Unsupported method: {method}")

        # 500 is a bug - fail the test
        if resp.status_code == 500:
            pytest.fail(
                f"500 INTERNAL SERVER ERROR: {method} {path}\n"
                f"Response: {resp.text[:500]}"
            )
        # 502/504 are bugs
        if resp.status_code in {502, 504}:
            pytest.fail(f"{resp.status_code} SERVER ERROR: {method} {path}")
        # 503 on scheduler endpoints = scheduler not running, skip
        if resp.status_code == 503 and "scheduler" in path:
            pytest.skip("Scheduler not running")
